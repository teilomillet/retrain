@dataclass
class PhaseResult:
    phase: str
    status: str  # "pass", "fail", "skip", "error"
    duration_s: float = 0.0
    details: dict = field(default_factory=dict)


def _run_retrain(toml_path: str, extra_args: list[str] | None = None) -> int:
    cmd = [sys.executable, "-m", "retrain", toml_path]
    if extra_args:
        cmd.extend(extra_args)
    print(f"  $ {' '.join(cmd)}")
    proc = subprocess.run(cmd, cwd=str(Path(__file__).resolve().parent.parent))
    return proc.returncode


def _find_last_checkpoint(log_dir: str) -> str | None:
    log_path = Path(log_dir)
    if not log_path.exists():
        return None
    checkpoints = sorted(
        log_path.glob("checkpoint_step_*"),
        key=lambda p: int(p.name.split("_")[-1]) if p.name.split("_")[-1].isdigit() else 0,
    )
    return str(checkpoints[-1]) if checkpoints else None


def _run_benchmark(
    model: str,
    seeds: list[int],
    http_url: str,
    max_turns: int,
    output_path: str,
    adapter_path: str | None = None,
) -> list[dict]:
    """Record benchmark config. Must be filled by running bench_energy.py manually."""
    output = Path(output_path)
    output.parent.mkdir(parents=True, exist_ok=True)

    bench_config = {
        "model": model,
        "adapter_path": adapter_path,
        "seeds": seeds,
        "http_url": http_url,
        "max_turns": max_turns,
        "domain": "energy",
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "status": "pending",
        "instructions": (
            "Run benchmark manually:\n"
            f"  uv run python python/scripts/bench_energy.py"
            + (f" --adapter {adapter_path}" if adapter_path else "")
            + f"\n  Results → {output_path}"
        ),
    }
    with open(output, "w") as f:
        json.dump(bench_config, f, indent=2)

    return []


def _check_sft_gate(
    bench_path: str,
    min_avg_score: float,
) -> tuple[bool, dict]:
    path = Path(bench_path)
    if not path.exists():
        return False, {"reason": "benchmark results not found", "path": bench_path}

    with open(path) as f:
        data = json.load(f)

    if data.get("status") == "pending":
        return False, {
            "reason": "benchmark not yet run — run it manually, then re-execute pipeline with --skip-sft",
            "instructions": data.get("instructions", ""),
        }

    results = data if isinstance(data, list) else data.get("results", [])
    if not results:
        return False, {"reason": "no benchmark results found"}

    avg_score = sum(r.get("score", 0) for r in results) / len(results)

    passed = avg_score >= min_avg_score
    details = {
        "total_seeds": len(results),
        "avg_score": round(avg_score, 4),
        "min_avg_score_required": min_avg_score,
        "passed": passed,
    }
    return passed, details


def _resolve_export_path(base_dir: Path, value: str) -> Path:
    path = Path(value).expanduser()
    if path.is_absolute():
        return path.resolve()
    return (base_dir / path).resolve()


def _load_approved_export_handoff(export_index_path: str | Path) -> tuple[Path, JSONDict]:
    path = Path(export_index_path).expanduser().resolve()
    if not path.exists():
        raise FileNotFoundError(f"approved export bundle not found: {path}")
    data = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(data, dict):
        raise ValueError(f"approved export bundle must be a JSON object: {path}")
    return path, data


def _apply_approved_export_handoff(
    cfg: PipelineConfig,
    export_index_path: str | Path,
) -> JSONDict:
    """Apply an approved export bundle to the retrain pipeline config."""
    path, export_bundle = _load_approved_export_handoff(export_index_path)

    domain = export_bundle.get("domain")
    if isinstance(domain, str) and domain.strip() and domain != "energy":
        raise ValueError(
            f"approved export bundle is for domain {domain!r}, not 'energy'"
        )

    retrain_input = export_bundle.get("retrain_input", {})
    if not isinstance(retrain_input, dict):
        retrain_input = {}

    candidate = export_bundle.get("candidate", {})
    if not isinstance(candidate, dict):
        candidate = {}

    candidate_model = candidate.get("model")
    if isinstance(candidate_model, str) and candidate_model.strip():
        cfg.model = candidate_model

    retrain_domain = retrain_input.get("domain")
    if isinstance(retrain_domain, str) and retrain_domain.strip() and retrain_domain != "energy":
        raise ValueError(
            f"approved export retrain input targets {retrain_domain!r}, not 'energy'"
        )

    sft_data_path = retrain_input.get("sft_data_path")
    if not isinstance(sft_data_path, str) or not sft_data_path.strip():
        raise ValueError("approved export bundle is missing retrain_input.sft_data_path")
    cfg.sft_data_path = str(_resolve_export_path(path.parent, sft_data_path))
    cfg.approved_export_path = str(path)

    export_id = export_bundle.get("export_id")
    if not isinstance(export_id, str) or not export_id.strip():
        export_id = path.stem
    if not cfg.artifact_dir:
        cfg.artifact_dir = str(path.parent / "retrain_runs" / export_id)

    return export_bundle


# ---------------------------------------------------------------------------
# Pipeline state
# ---------------------------------------------------------------------------

@dataclass
class PipelineState:
    config: dict = field(default_factory=dict)
    phases: list[dict] = field(default_factory=list)
    current_phase: str = "sft"
    sft_checkpoint: str = ""
    rl_checkpoint: str = ""
    started_at: str = ""
    finished_at: str = ""
    status: str = "running"

    def save(self, path: str) -> None:
        with open(path, "w") as f:
            json.dump(asdict(self), f, indent=2, default=str)

    @classmethod
    def load(cls, path: str) -> PipelineState:
        with open(path) as f:
            data = json.load(f)
        state = cls()
        for k, v in data.items():
            if hasattr(state, k):
                setattr(state, k, v)
        return state


