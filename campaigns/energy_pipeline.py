#!/usr/bin/env python3
"""Energy training pipeline: SFT → quality gate → RL → quality gate.

One command, one artifact directory, all phases orchestrated.

Usage:
    python campaigns/energy_pipeline.py
    python campaigns/energy_pipeline.py --artifact-dir runs/energy-001
    python campaigns/energy_pipeline.py --skip-sft --sft-checkpoint runs/energy-001/sft/checkpoint_step_60
    python campaigns/energy_pipeline.py --approved-export approved_candidate_exports/job-123/export.json
    python campaigns/energy_pipeline.py --dry-run

Prerequisites:
    1. Generate SFT data (if not already done):
       cargo run --release --bin soma_http -- configs/http/soma-energy-local.toml
       cd python && uv run python scripts/generate_sft_data_universal.py configs/sft/energy-search.toml

    2. Start the HTTP kernel for RL:
       cargo run --release --bin soma_http -- configs/http/soma-energy-local.toml

Artifact directory structure:
    {artifact_dir}/
    ├── pipeline.json        # full pipeline config + status + results
    ├── sft/                 # Phase 1: SFT checkpoints + logs
    │   ├── sft.toml
    │   ├── metrics.jsonl
    │   └── checkpoint_step_60/
    ├── bench-sft/           # Phase 2: SFT quality gate benchmark
    │   └── results.json
    ├── rl/                  # Phase 3: RL checkpoints + logs
    │   ├── rl.toml
    │   ├── metrics.jsonl
    │   └── checkpoint_step_*/
    └── bench-rl/            # Phase 4: RL quality gate benchmark
        └── results.json

Algorithm: REINFORCE++ with batch advantage normalization
  - Critic-free (same memory as GRPO)
  - Global batch normalization (fixes GRPO's biased local estimator)
  - Asymmetric clipping ε_low=0.2, ε_high=0.28 (DAPO clip-higher)
  - Per-turn rewards from energy domain (profit-based scoring)
"""

