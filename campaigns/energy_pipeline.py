def run_pipeline(
    cfg: PipelineConfig,
    *,
    dry_run: bool = False,
    skip_sft: bool = False,
    sft_checkpoint: str = "",
) -> PipelineState:
    """Run the full energy training pipeline."""

    if not cfg.artifact_dir:
        ts = datetime.now().strftime("%Y%m%d-%H%M%S")
        cfg.artifact_dir = f"runs/energy-{ts}"

    art = Path(cfg.artifact_dir)
    art.mkdir(parents=True, exist_ok=True)

    state_path = str(art / "pipeline.json")

    if Path(state_path).exists():
        state = PipelineState.load(state_path)
        print(f"Resuming pipeline from phase: {state.current_phase}")
    else:
        state = PipelineState(
            config=asdict(cfg),
            started_at=datetime.now(timezone.utc).isoformat(),
        )

    sft_dir = str(art / "sft")
    rl_dir = str(art / "rl")
    bench_sft_path = str(art / "bench-sft" / "results.json")
    bench_rl_path = str(art / "bench-rl" / "results.json")

    run_tag = state.config.get("run_tag", "") if state.phases else ""
    if not run_tag:
        run_tag = _run_tag(cfg)
        state.config["run_tag"] = run_tag

    # Verify SFT data exists before starting
    sft_data = Path(cfg.sft_data_path)
    if not sft_data.exists() and not skip_sft:
        print(f"ERROR: SFT data not found at {cfg.sft_data_path}")
        print(f"\nGenerate it first:")
        print(f"  cargo run --release --bin soma_http -- configs/http/soma-energy-local.toml")
        print(f"  cd python && uv run python scripts/generate_sft_data_universal.py ../configs/sft/energy-search.toml")
        return PipelineState(status="failed")

    # Write manifest
    manifest_path = str(art / "manifest.json")
    if not Path(manifest_path).exists():
        manifest = {
            "pipeline": "energy",
            "run_tag": run_tag,
            "created_at": datetime.now(timezone.utc).isoformat(),
            "config": asdict(cfg),
            "algorithm": {
                "name": "REINFORCE++ with Baseline",
                "paper": "arxiv:2501.03262",
                "advantage_mode": "reinforce_pp",
                "batch_advantage_norm": True,
                "evidence": [
                    "SFT-first optimal: Wang & Ammanabrolu (2510.01132)",
                    "REINFORCE++ > GRPO & PPO on agents: Hu (2501.03262)",
                    "No sigma normalization: ScaleRL, Dr. GRPO",
                ],
            },
            "phases": [
                {"name": "sft", "steps": cfg.sft_steps, "lr": cfg.sft_lr},
                {"name": "bench-sft", "gate": {"min_avg_score": cfg.sft_gate_min_avg_score}},
                {"name": "rl", "steps": cfg.rl_steps, "lr": cfg.rl_lr,
                 "batch_advantage_norm": True, "adv_clip_max": cfg.rl_adv_clip_max},
                {"name": "bench-rl"},
            ],
        }
        with open(manifest_path, "w") as f:
            json.dump(manifest, f, indent=2)

    print(f"\n{'='*70}")
    print(f"  ENERGY TRAINING PIPELINE")
    print(f"  Artifact: {cfg.artifact_dir}")
    print(f"  Run tag:  {run_tag}")
    print(f"  Model:    {cfg.model}")
    print(f"  Backend:  {cfg.backend}")
    if cfg.approved_export_path:
        print(f"  Export:   {cfg.approved_export_path}")
    print(f"  SFT:      {cfg.sft_steps} steps (lr={cfg.sft_lr})")
    print(f"  RL:       {cfg.rl_steps} steps (lr={cfg.rl_lr})")
    print(f"  Algorithm: REINFORCE++ + batch_advantage_norm")
    if cfg.wandb_project:
        print(f"  W&B:      {cfg.wandb_project} (group={run_tag})")
    print(f"{'='*70}\n")

    if dry_run:
        print("[DRY RUN] Would execute the following phases:\n")

    # ── Phase 1: SFT ──────────────────────────────────────────────────────
    if skip_sft or state.current_phase in ("bench-sft", "rl", "bench-rl", "done"):
        checkpoint = sft_checkpoint or state.sft_checkpoint
        if not checkpoint:
            print("ERROR: --skip-sft requires --sft-checkpoint")
            state.status = "failed"
            state.save(state_path)
            return state
        print(f"[Phase 1: SFT] SKIPPED — using checkpoint: {checkpoint}")
        state.sft_checkpoint = checkpoint
    else:
        print(f"[Phase 1: SFT] {cfg.sft_steps} steps on oracle demonstrations")
        sft_toml_path = str(art / "sft.toml")
        toml_content = _sft_toml(cfg, sft_dir, run_tag)

        if dry_run:
            print(f"  Would write: {sft_toml_path}")
            print(f"  Would run:   retrain {sft_toml_path}")
        else:
            Path(sft_toml_path).write_text(toml_content)
            t0 = time.time()
            rc = _run_retrain(sft_toml_path)
            duration = time.time() - t0

            if rc != 0:
                result = PhaseResult("sft", "error", duration, {"return_code": rc})
                state.phases.append(asdict(result))
                state.status = "failed"
                state.save(state_path)
                print(f"\n  SFT FAILED (exit code {rc})")
                return state

            checkpoint = _find_last_checkpoint(sft_dir)
            if not checkpoint:
                print(f"\n  ERROR: No checkpoint found in {sft_dir}")
                state.status = "failed"
                state.save(state_path)
                return state

            state.sft_checkpoint = checkpoint
            state.current_phase = "bench-sft"
            result = PhaseResult("sft", "pass", duration, {"checkpoint": checkpoint})
            state.phases.append(asdict(result))
            state.save(state_path)
            print(f"\n  SFT complete: {checkpoint} ({duration:.0f}s)")

    # ── Phase 2: SFT Quality Gate ─────────────────────────────────────────
    if state.current_phase in ("rl", "bench-rl", "done"):
        print(f"[Phase 2: SFT Quality Gate] SKIPPED — already passed")
    else:
        print(f"\n[Phase 2: SFT Quality Gate] Benchmark on seeds {cfg.bench_seeds}")

        if dry_run:
            print(f"  Would benchmark: {state.sft_checkpoint or '<pending>'}")
            print(f"  Would save to:   {bench_sft_path}")
            print(f"  Gate: avg score ≥{cfg.sft_gate_min_avg_score}")
        else:
            _run_benchmark(
                model=cfg.model,
                seeds=cfg.bench_seeds,
                http_url=cfg.http_url,
                max_turns=cfg.bench_max_turns,
                output_path=bench_sft_path,
                adapter_path=state.sft_checkpoint,
            )

            passed, details = _check_sft_gate(
                bench_sft_path,
                cfg.sft_gate_min_avg_score,
            )

            result = PhaseResult(
                "bench-sft",
                "pass" if passed else "paused",
                details=details,
            )
            state.phases.append(asdict(result))

            if not passed:
                state.status = "paused"
                state.current_phase = "bench-sft"
                state.save(state_path)
                print(f"\n  SFT Quality Gate: PAUSED")
                print(f"  {details.get('reason', 'Gate not passed')}")
                print(f"\n  Next steps:")
                print(f"  1. Run benchmark manually")
                print(f"  2. Save results to: {bench_sft_path}")
                print(f"  3. Re-run: python {__file__} --artifact-dir {cfg.artifact_dir}")
                return state

            state.current_phase = "rl"
            state.save(state_path)
            print(f"\n  SFT Quality Gate: PASSED {details}")

    # ── Phase 3: RL ───────────────────────────────────────────────────────
    if state.current_phase in ("bench-rl", "done"):
        print(f"[Phase 3: RL] SKIPPED — already complete")
    else:
        print(f"\n[Phase 3: RL] {cfg.rl_steps} steps with REINFORCE++ + batch norm")
        rl_toml_path = str(art / "rl.toml")
        toml_content = _rl_toml(cfg, rl_dir, run_tag)

        if dry_run:
            print(f"  Would write: {rl_toml_path}")
            print(f"  Would run:   retrain {rl_toml_path} --resume-from {state.sft_checkpoint or '<pending>'}")
        else:
            Path(rl_toml_path).write_text(toml_content)
            t0 = time.time()
            rc = _run_retrain(rl_toml_path, ["--resume-from", state.sft_checkpoint])
            duration = time.time() - t0

            if rc != 0:
                result = PhaseResult("rl", "error", duration, {"return_code": rc})
                state.phases.append(asdict(result))
                state.status = "failed"
                state.save(state_path)
                print(f"\n  RL FAILED (exit code {rc})")
                return state

            checkpoint = _find_last_checkpoint(rl_dir)
            state.rl_checkpoint = checkpoint or ""
            state.current_phase = "bench-rl"
            result = PhaseResult("rl", "pass", duration, {"checkpoint": checkpoint})
            state.phases.append(asdict(result))
            state.save(state_path)
            print(f"\n  RL complete: {checkpoint} ({duration:.0f}s)")

    # ── Phase 4: RL Quality Gate ──────────────────────────────────────────
    if state.current_phase == "done":
        print(f"[Phase 4: RL Quality Gate] SKIPPED — already complete")
    else:
        print(f"\n[Phase 4: RL Quality Gate] Benchmark on seeds {cfg.bench_seeds}")

        if dry_run:
            print(f"  Would benchmark: {state.rl_checkpoint or '<pending>'}")
            print(f"  Would compare:   SFT-only vs SFT+RL")
        else:
            _run_benchmark(
                model=cfg.model,
                seeds=cfg.bench_seeds,
                http_url=cfg.http_url,
                max_turns=cfg.bench_max_turns,
                output_path=bench_rl_path,
                adapter_path=state.rl_checkpoint,
            )

            result = PhaseResult("bench-rl", "pass", details={
                "sft_checkpoint": state.sft_checkpoint,
                "rl_checkpoint": state.rl_checkpoint,
            })
            state.phases.append(asdict(result))
            state.current_phase = "done"
            state.status = "completed"
            state.finished_at = datetime.now(timezone.utc).isoformat()
            state.save(state_path)

    # ── Summary ───────────────────────────────────────────────────────────
    print(f"\n{'='*70}")
    print(f"  PIPELINE {state.status.upper()}")
    print(f"  Artifact: {cfg.artifact_dir}")
    print(f"  Run tag:  {run_tag}")
    if state.sft_checkpoint:
        print(f"  SFT checkpoint: {state.sft_checkpoint}")
    if state.rl_checkpoint:
        print(f"  RL checkpoint:  {state.rl_checkpoint}")
    print()
    for p in state.phases:
        status_icon = {"pass": "+", "fail": "x", "error": "!", "paused": "~", "skip": "-"}.get(p["status"], "?")
        print(f"    [{status_icon}] {p['phase']}: {p['status']} ({p.get('duration_s', 0):.0f}s)")

    state.save(state_path)

    # Show artifact tree
    print(f"\n  Artifact tree:")
    for item in sorted(art.rglob("*")):
        if item.is_file() and ".git" not in str(item):
            rel = item.relative_to(art)
            size = item.stat().st_size
            if size > 1_000_000:
                size_str = f"{size / 1_000_000:.1f}MB"
            elif size > 1_000:
                size_str = f"{size / 1_000:.0f}KB"
            else:
                size_str = f"{size}B"
            print(f"    {rel}  ({size_str})")
    print(f"{'='*70}\n")

    return state


