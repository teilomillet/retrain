@dataclass
class PhaseResult:
    phase: str
    status: str  # "pass", "fail", "skip", "error"
    duration_s: float = 0.0
    details: dict = field(default_factory=dict)


def _run_retrain(toml_path: str, extra_args: list[str] | None = None) -> int:
    """Run `retrain <toml>` as subprocess, streaming output."""
    cmd = [sys.executable, "-m", "retrain", toml_path]
    if extra_args:
        cmd.extend(extra_args)
    print(f"  $ {' '.join(cmd)}")
    proc = subprocess.run(cmd, cwd=str(Path(__file__).resolve().parent.parent))
    return proc.returncode


def _find_last_checkpoint(log_dir: str) -> str | None:
    """Find the latest checkpoint_step_N directory."""
    log_path = Path(log_dir)
    if not log_path.exists():
        return None
    checkpoints = sorted(
        log_path.glob("checkpoint_step_*"),
        key=lambda p: int(p.name.split("_")[-1]) if p.name.split("_")[-1].isdigit() else 0,
    )
    return str(checkpoints[-1]) if checkpoints else None


def _run_benchmark(
    model: str,
    seeds: list[int],
    http_url: str,
    max_turns: int,
    output_path: str,
    adapter_path: str | None = None,
) -> list[dict]:
    """Run warehouse benchmark episodes and save results.

    This calls run_episode directly (no OpenRouter needed for local models).
    For local adapter evaluation, we'd need vLLM or similar serving.
    For now, we record the benchmark config and return placeholder results
    that must be filled by running bench_warehouse.py manually.
    """
    output = Path(output_path)
    output.parent.mkdir(parents=True, exist_ok=True)

    bench_config = {
        "model": model,
        "adapter_path": adapter_path,
        "seeds": seeds,
        "http_url": http_url,
        "max_turns": max_turns,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "status": "pending",
        "instructions": (
            "Run benchmark manually:\n"
            f"  python python/scripts/bench_warehouse.py"
            + (f" --adapter {adapter_path}" if adapter_path else "")
            + f"\n  Results → {output_path}"
        ),
    }
    with open(output, "w") as f:
        json.dump(bench_config, f, indent=2)

    return []


def _check_sft_gate(
    bench_path: str,
    min_profitable: int,
    min_avg_score: float,
) -> tuple[bool, dict]:
    """Check SFT quality gate from benchmark results.

    Returns (passed, details).
    """
    path = Path(bench_path)
    if not path.exists():
        return False, {"reason": "benchmark results not found", "path": bench_path}

    with open(path) as f:
        data = json.load(f)

    if data.get("status") == "pending":
        return False, {
            "reason": "benchmark not yet run — run it manually, then re-execute pipeline with --skip-sft",
            "instructions": data.get("instructions", ""),
        }

    results = data if isinstance(data, list) else data.get("results", [])
    if not results:
        return False, {"reason": "no benchmark results found"}

    profitable = sum(1 for r in results if r.get("profit_euros", 0) > 0)
    avg_score = sum(r.get("score", 0) for r in results) / len(results)

    passed = profitable >= min_profitable and avg_score >= min_avg_score
    details = {
        "profitable_seeds": profitable,
        "total_seeds": len(results),
        "avg_score": round(avg_score, 4),
        "min_profitable_required": min_profitable,
        "min_avg_score_required": min_avg_score,
        "passed": passed,
    }
    return passed, details


