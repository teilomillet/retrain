@dataclass
class PipelineConfig:
    """All knobs for the full pipeline in one place."""

    # -- Artifact --
    artifact_dir: str = ""  # auto-generated if empty

    # -- Backend --
    backend: str = "tinker"  # tinker (remote GPU) or local

    # -- Model --
    model: str = "Qwen/Qwen3-4B-Instruct-2507"
    lora_rank: int = 32

    # -- SFT phase --
    sft_steps: int = 60
    sft_lr: float = 4e-5
    sft_batch_size: int = 4
    sft_data_path: str = "/Users/teilomillet/Code/soma/python/scripts/warehouse_sft_data.jsonl"

    # -- RL phase --
    rl_steps: int = 200
    rl_lr: float = 1e-5
    rl_batch_size: int = 4
    rl_group_size: int = 8
    rl_adv_clip_max: float = 5.0
    rl_temperature: float = 0.7

    # -- Environment --
    http_url: str = "http://127.0.0.1:3000"
    max_turns: int = 15
    num_examples: int = 16
    dataset_seed: int = 42

    # -- Benchmark --
    # Eval seeds must NOT overlap with training seeds [42..57]
    bench_seeds: list[int] = field(default_factory=lambda: [100, 200, 300])
    bench_max_turns: int = 3000

    # -- Quality gates --
    sft_gate_min_profitable_seeds: int = 2  # out of len(bench_seeds)
    sft_gate_min_avg_score: float = 0.1     # normalized [0,1]

    # -- Tracking --
    wandb_project: str = ""  # empty = no wandb
    wandb_entity: str = ""

    # -- Misc --
    seed: int = 42
    save_every: int = 20
    max_tokens: int = 10240


