def _run_tag(cfg: PipelineConfig) -> str:
    """Short unique tag for this pipeline run."""
    ts = datetime.now().strftime("%m%d-%H%M")
    model_short = cfg.model.split("/")[-1][:12]
    return f"wh-{model_short}-s{cfg.seed}-{ts}"


