#!/usr/bin/env python3
"""Warehouse training pipeline: SFT → quality gate → RL → quality gate.

One command, one artifact directory, all phases orchestrated.

Usage:
    python campaigns/warehouse_pipeline.py
    python campaigns/warehouse_pipeline.py --artifact-dir runs/warehouse-001
    python campaigns/warehouse_pipeline.py --skip-sft --sft-checkpoint runs/warehouse-001/sft/checkpoint_step_60
    python campaigns/warehouse_pipeline.py --dry-run

Artifact directory structure:
    {artifact_dir}/
    ├── pipeline.json        # full pipeline config + status + results
    ├── sft/                 # Phase 1: SFT checkpoints + logs
    │   ├── sft.toml
    │   ├── metrics.jsonl
    │   └── checkpoint_step_60/
    ├── bench-sft/           # Phase 2: SFT quality gate benchmark
    │   └── results.json
    ├── rl/                  # Phase 3: RL checkpoints + logs
    │   ├── rl.toml
    │   ├── metrics.jsonl
    │   └── checkpoint_step_*/
    └── bench-rl/            # Phase 4: RL quality gate benchmark
        └── results.json

Algorithm: REINFORCE++ with batch advantage normalization
  - Critic-free (same memory as GRPO)
  - Global batch normalization (fixes GRPO's biased local estimator)
  - Asymmetric clipping ε_low=0.2, ε_high=0.28 (DAPO clip-higher)
  - Dense per-turn rewards from warehouse domain

Evidence base:
  - SFT-first: 60 demos + RL is optimal (Wang & Ammanabrolu, 2510.01132)
  - REINFORCE++ > GRPO and PPO on agent tasks (Hu, 2501.03262)
  - σ normalization hurts at scale (ScaleRL, DAPO, Dr. GRPO)
  - Asymmetric clipping prevents entropy collapse (DAPO)
"""

